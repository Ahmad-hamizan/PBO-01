package Praktikum02.Dasar;

// While
public class Looping2 {
    public static void main(String[] args) {
        System.out.println("cetak angka 10 s/d 5");
        int a = 10;
        while (a >= 5) {
            System.out.println("Angka " + a);
            a--; // decrement a = a - 1
        }
    }
}
